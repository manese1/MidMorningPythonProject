#Arithmetic operators - Simple calculations
x = 34
y = 20
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) #Modulus=Returns the remainder

#Comparison Operators - Compare values
print(x<y)
print(x>y)
print(x==y)
print(x!=y) #Not equal to
print(x>=y)
print(x<=y)

#Assignment Operators
number = 10
print(number)

#Operator precedence
number += 2
print(number)

#Logical Operators
num1 = 60
num2 = 12
print(num1==num2 and num1>num2)
print(num1==num2 or num1>num2)
print(not(num1==num2 or num1>num2))
print()
