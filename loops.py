#While loops
n = 100
while n <= 105:
    print(n)
    n += 1

a = 10
while a >= 5:
    print("Number:", a)
    a -=1
print()

#For Loop
for b in range(100, 106):
    print(b)
print()

for c in range(106, 100, -1):
    print(c)
print()

languages = ["Python", "Java", "JavaScript"]
print(languages)
for language in languages:
    print(language)

